import random
import discord
import asyncio
import time
from scripts.dbmanagement.SQLiteDBHandler import update_points


async def playNumberGuesser(ctx, client):
    rand = random.randrange(1,11)
    member = ctx.message.author
    #await ctx.send(f"{member.mention}, you are a {rand}!")
    await ctx.send(f"{member.mention}, guess a number between 1 and 10!")

    # This will make sure that the response will only be registered if the following
    # conditions are met:
    def check(msg):
        return msg.author == ctx.author and msg.channel == ctx.channel and msg.content

    try:
        msg = await client.wait_for("message", check=check)
        number = int(msg.content)

        if (number < rand):
            await ctx.send(f"{member.mention}, you guessed too low, you are a {rand}!")
        elif (number > rand):
            await ctx.send(f"{member.mention}, you guessed too high, you are a {rand}!")
        else:
            await ctx.send(f"{member.mention}, you are a {rand}! :blush:")
            # Award 10 points
            userID = ctx.message.author.id
            update_points(userID, int(10))

    except ValueError:
            await ctx.send(f"That's not a number! Your number was {rand}!")
            amogus = buildAmogus(rand)
            await ctx.send(f"{amogus}")

    except asyncio.TimeoutError:
        await ctx.send("Reply faster next time!")


